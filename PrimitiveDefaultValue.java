public class PrimitiveDefaultValue{
    public static void main(String[] args) {
        int a = 0;
        System.out.println(a);
        float b = 0f;
        System.out.println(b);
        char c = 'a';
        System.out.println(c);
        boolean d = false;
        System.out.println(d);
    }
}